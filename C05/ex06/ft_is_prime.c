int	ft_is_prime(int nb)
{
	int i;

	if (nb <= 1)
		return (0);
	if (nb <= 3)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0)
		return (0);
	i = 5;
	if (nb > 3)
	{
		while (i * i <= nb && i * i > 0)
		{
			if (nb % i == 0)
				return (0);
			i += 2;
		}
	}
	return (1);
}

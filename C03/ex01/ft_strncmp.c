int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i++ < n)
	{
		if (*s1 != *s2)
			return (*s1 < *s2 ? -1 : 1);
		if (*s1 == 0 && *s2 == 0)
			break ;
		s1++;
		s2++;
	}
	return (0);
}
